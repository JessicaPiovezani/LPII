valor = int (input("Digite um número inteiro: "))

if valor >= 0:

    if valor %2 == 0:
        print ("O número é positivo e é par.")
    else:
        print ("O número é postivo e é impar")


else:

    if valor %2 == 0:
         print ("O número é negativo e é par.")
    else:
        print ("O número é negativo e é impar")
